// While trong TypeScript cũng hoạt động tương tự như trong JavaScript.
// Giống như for loop, while loop cũng được sử dụng để lặp qua một khối mã nếu điều kiện được cung cấp là true.
// Nếu như với for loop, chúng ta biết được số lần lặp cụ thể, thì với while loop, chúng ta không biết trước số lần lặp.
// Ví dụ:
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}